<script setup>
import { nextTick, onMounted, ref, watch } from 'vue'
import bg18 from "@/assets/slides-images/eighteen/bg.webp"

// eslint-disable-next-line no-undef
const emit = defineEmits(['continue'])

const replicStepAskWaiting = ref(false)

const nextStep = () => {
    replicStepAskWaiting.value = false
    replicStep.value += 1
}

let text1, text2

const replicStep = ref(0)

watch(replicStep, (value) => {
    switch (value) {
        case 1:
            text2.classList.add('visible')
            break;

        case 2:
            emit('continue')
            break;

        default:
            break;
    }
    
})

onMounted(async () => {
    await nextTick()

    document.addEventListener('nextReplic', () => {
        if (replicStepAskWaiting.value === true) return 
        replicStep.value += 1
    })

    text1 = document.querySelector('#text-181')
    text2 = document.querySelector('#text-182')

    setTimeout(() => {
        text1.classList.add('visible')
    }, 500)
})

// eslint-disable-next-line no-undef
defineExpose({
    nextStep
})
</script>

